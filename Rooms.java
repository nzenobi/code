package concepts;

public class Rooms {
	
	public Rooms (String name, String examine, String use, String take, String go, String drop, String on)
	{
		this.examine = examine;
		this.use = use;
		this.take = take;
		this.go = go; 
		this.drop = drop;
		this.on = on;
	}
	private String name;
	private String examine;
	private String use;
	private String take;
	private String go;
	private String drop;
	private String on;
}
